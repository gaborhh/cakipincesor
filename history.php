A Cáki
Pincesor legrégebbi épülete egyébként 1778-ban épült, a többsége pedig az 1800-as évek
elején.

Maga a pincesor épületei a telken kívüli bortároló építmények legrégebbi
változatának a képviselői.
Földfelszín felettiek és egysejtűek – így áll a hivatalos
jellemzésében a leírásuk.
Falazatuk boronafal, tetejük zsúpfedeles mely elől csonkakonytos,
hátul pedig úgynevezett „farazatos”, gerincük pedig sározott zsúp fedi.
Csonkakontyos
tetejük előreugrik a homlokzat elé.
    </p>
